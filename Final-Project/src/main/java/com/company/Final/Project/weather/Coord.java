package com.company.Final.Project.weather;

public class Coord {

    // attributes
    private String lon;
    private String lat;

    // getters and setters
    public String getLon() {
        return lon;
    }

    public String getLat() {
        return lat;
    }

    public void setLon(String lon) {
        this.lon = lon;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }
}
